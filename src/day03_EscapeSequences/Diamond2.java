package day03_EscapeSequences;

public class Diamond2 {

    public static void main(String[] args) {

        // print a different Diamond

        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  / * \\");
        System.out.println(" / * * \\");
        System.out.println("/ * * * \\");
        System.out.println("---------");
        System.out.println("\\ * * * /");
        System.out.println(" \\ * * /");
        System.out.println("  \\ * /");
        System.out.println("   \\ /");
        System.out.println("    v");

    }

}
