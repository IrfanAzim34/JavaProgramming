package day03_EscapeSequences;

public class Diamond {

    public static void main(String[] args) {

        // print a Diamond

        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  /   \\");
        System.out.println(" /     \\");
        System.out.println("/       \\");
        System.out.println("---------");
        System.out.println("\\       /");
        System.out.println(" \\     /");
        System.out.println("  \\   /");
        System.out.println("   \\ /");
        System.out.println("    v");

    }

}
